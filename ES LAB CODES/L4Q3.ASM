	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x40001000;
	DCD Reset_Handler;
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler	
	LDR R0,=ARRAY
	LDR R1,=POS
	LDR R3,=NUM
	LDR R3,[R3]
	MOV R2,#0
	LDR R4,=10
UP
	LDR R5,[R0],#4
	CMP R5,R3
	BEQ FND
	ADD R2,#1
	SUB R4,#1
	TEQ R4,#0
	BNE UP
	MOV R2,#0XFF ;to indicate not found
FND	STRB R2,[R1]	
STOP B STOP
ARRAY DCD 0X18,0XAB,0X85,0XFF,0X01,0X52,0X86,0XDE,0X63,0X77
	AREA data, DATA, READWRITE
NUM DCD 0X86
POS DCD 0
	END