	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x40001000;
	DCD Reset_Handler;
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler	
	LDR R0,=NUM
	LDR R1,[R0]
	LDR R13,=0X10005000
	BL FACT
	LDR R8,=RES
	STR R2,[R8]
STOP B STOP
FACT
	CMP R1,#1
	BEQ EXIT
	PUSH {R1}
	PUSH {LR}
	SUB R1,#1
	BL FACT
	POP {LR}
	POP {R1}
	MUL R2,R1,R2
	BX LR
EXIT MOV R2,#1
	BX LR
	
NUM DCD 0X8
	AREA data, DATA, READWRITE
RES DCD 0
	END