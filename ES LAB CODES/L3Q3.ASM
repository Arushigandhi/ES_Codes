	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x40001000;
	DCD Reset_Handler;
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler	
	LDR R0,=NUM
	LDR R1,=RESULT
	LDR R3,[R0]
	MOV R5,#32
UP	CMP R3,0XA
	BLO DOWN
	ADD R4,#1
	SUB R3,#0XA
	B UP
DOWN 
	ORR R6,R3
	ROR R6,#4
	SUB R5,#4
	CMP R4,#0
	MOV R3,R4
	MOV R4,#0
	BNE UP
	LSR R6,R5
	STR R6,[R1]
	
STOP B STOP
NUM DCD 0x00000FF
	AREA data, DATA, READWRITE
RESULT DCD 0
	END