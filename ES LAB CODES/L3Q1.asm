	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x40001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler	
	LDR R0,=NUM
	LDR R3,=RESULT
	ADD R3,#1 ;to store lsb first
	LDRB R1,[R0]
	AND R2,R1,#0x0F 
	CMP R2,#09 
	BLO DOWN
	ADD R2,#07 
DOWN
	ADD R2,#0x30 
	STRB R2,[R3],#-1 
	AND R2,R1,#0xF0 
	LSR R2,#4
	CMP R2,#09 
	BLO DOWN1
	ADD R2,#07
DOWN1
	ADD R2,#0x30 
	STRB R2,[R3]
STOP B STOP
NUM DCD 0x000003A
	AREA data, DATA, READWRITE
RESULT DCD 0,0
	END